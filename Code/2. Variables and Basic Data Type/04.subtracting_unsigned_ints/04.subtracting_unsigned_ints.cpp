#include <iostream>

int main() {
    unsigned u1 = 42, u2 = 10;

    std::cout << "u1 - u2 : " << u1 - u2 << std::endl;
    std::cout << "u2 - u1 : " << u2 - u1 << std::endl;

    return 0;
}

/*
RESULT :
u1 - u2 : 32
u2 - u1 : 4294967264
*/